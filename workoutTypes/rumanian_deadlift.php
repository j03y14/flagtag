  <div class="detail">


    <div class="how-to">
      <ol>
        <li>어깨보다 조금 넓은 간격으로 손등이 자신을 향하도록 바벨을 잡는다.</li>
        <li>다리를 어깨 넓이만큼 벌리고 무릎을 굽혀 하체를 고정한다.</li>
        <li>하체는 고정한 상태에서 상체를 앞으로 구부린다.</li>
        <li>무게 중심이 앞쪽으로 갔을 때 중량을 밑으로 내리기 위해 고관절을 뒤로 뺀다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 등근육의 긴장을 유지한다.<br>
        <span>|</span> 허리가 굽어지지 않도록 척추의 중립을 유지한다. <br>
        <span>|</span> 바가 몸에서 떨어지지 않도록 주의 한다. <br>
        <span>|</span> 무릎아래 다리가 항상 바닥과 수직이 되도록 유지한다. <br>
      </div>
