    <div class="how-to">
      <ol>
        <li>손등이 자신을 향하도록 철봉을 잡고 견갑대를 모은 상태로 가슴을 펴고 상 45도 정도로 시선을 유지한다.</li>
        <li>견갑대의 상태를 유지한 상태로 철봉을 당겨 몸을 끌어 올린다.</li>
        <li>천천히 내려온다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 오르 내릴 때 견갑대의 상태를 유지한다.<br>
        <span>|</span> 너무 빠르게 힘을 풀면서 내려오지 않는다. <br>
        <span>|</span> 양 팔의 너비를 조절하여 자극 부위를 조절할 수 있다. <br>
