      <ol>
        <li>발을 골반 너비로 벌리고 서서 승모근에 바벨을 올려놓는다.</li>
        <li>무릎을 약간 굽힌 후 허리와 가슴을 편 상태에서 등을 곧게 유지하고 상체를 앞으로 기울여 준다. </li>
