  <div class="detail">


    <div class="how-to">
      <ol>
        <li>두 손으로 오버핸드 그립을 사용해 케틀벨 하나를 잡고 양발은 어깨너비로 벌리고 선다.</li>
        <li>견갑골을 모으고 골반을 뒤로 밀며 양무릎을 살짝 구부리면서 상체를 숙인다.</li>
        <li>양팔을 완전히 뻗어 케틀벨을 두 다리 사이로 늘어뜨린다.</li>
        <li>코어 근육과 엉덩이에 힘을 주면서 골반을 민다. </li>
        <li>팔 근육을 사용하지 않고 골반의 미는 힘을 이용해서 케틀벨을 어깨 높이까지 올린다.</li>
        <li>자연스럽게 케틀벨을 되돌리며 동작을 반복한다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 부상 방지를 위해 견갑골을 모은 상태를 유지한다.<br>
        <span>|</span> 동작 최고 지점까지 엉덩이 근육의 긴장을 유지한다. <br>
        <span>|</span> 허리가 굽지 않도록 주의한다. <br>
      </div>
    </div>

