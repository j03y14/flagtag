  <div class="detail">


    <div class="how-to">
      <ol>
        <li>바벨을 정강이에 최대한 붙인다.</li>
        <li>척추 중립을 유지한채 골반과 무릎을 구부려 바벨을 잡는다.</li>
        <li>몸과 바벨을 최대한 가깝게 한 상태로 그대로 들어올린다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 척추의 중립과 숄더패킹을 항상 유지한채로 등의 긴장을 풀지 않고 동작을 수행한다.<br>
        <span>|</span> 시선을 몸의 움직임과 함께 하고, 고개를 쳐들지 않도록 한다. <br>
        <span>|</span> 바벨이 땅에 있을 때 상체의 긴장을 충분히 만들어 준 채로 동작을 수행한다.<br>
        <span>|</span> 바벨을 최대한 몸과 붙인다<br>
