      <ol>
        <li>어깨 넓이보다 조금 더 넓게 바를 잡는다.</li>
        <li>바를 정강이에 최대한 붙여서 위치하게 하고 가슴을 활짝 펴서 등과 허리를 아치형태로 유지시킨다.</li>
