  <div class="detail">

    <h4 class='title'>맨몸 스쿼트</h4>
    <div class="how-to">
      <ol>
        <li>어깨 넓이로 발을 벌리고 양 팔을 자연스럽게 한다.</li>
        <li>숨을 들이쉬고 엉덩이를 뒤로 빼며 천천히 앉는다.</li>
        <li>허벅지와 바닥이 평행을 이룰 때까지 앉는다.</li>
        <li>숨을 뱉으며 상체와 하체가 함께 올라온다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 무게 중심을 발 바닥의 중앙에 유지하고 허리를 곧게 핀 상태에서 반복한다.
      </div>
    </div>

    <h4 class='title'>바벨 스쿼트</h4>
    <div class="how-to">
      <ol>
        <li>바벨을 승모근에 댄다.</li>
        <li>위팔과 아래 팔의 각도가 직각이 되도록 바벨을 잡는다.</li>
        <li>숨을 들이쉬며 허벅지와 바닥이 평행을 이룰 때까지 앉는다.</li>
        <li>숨을 뱉으며 상체와 하체가 함께 올라온다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 무게 중심을 발 바닥의 중앙에 유지하고 허리를 곧게 핀 세운 상태에서 반복한다. 손목이 꺾이지 않도 주의한다.
      </div>
    </div>

