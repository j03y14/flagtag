    <p>
      스쿼트는 앉았다 일어났다를 반복해 하반신의 근육량 증가에 커다란 효과가 있는 운동으로
      데드리프트, 벤치프레스와 함께 '3대 운동'이라 불리는 웨이트 트레이닝의 대표적인 운동이다.
