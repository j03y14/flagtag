  <div class="summary">
    <p>
      인클라인 벤치프레스는 45도에서 60도 정도로 경사진 벤치에 누워 바벨을 위쪽 방향으로 미는 운동이다.
    </p>
  </div>

  <div class="detail">


    <div class="how-to">
      <ol>
        <li>45도에서 60도 정도로 경사진 벤치에 누워 견갑대를 최대한 모으고 허리를 등방향으로 꺾어 벤치와 허리부분에 공간을 유지한다.</li>
        <li>바벨을 쇄골과 젖꼭지라인의 사이 가슴의 윗쪽 1/3 지점으로 가져온다.</li>
        <li>대흉근을 이용해 바벨을 밀어올린다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 견갑대를 최고점에서도 모은채로 유지한다.<br>
        <span>|</span> 바벨을 잡을 때, 손꿈치에 바벨이 지나가도록 잡아, 손목에 무리가 가지 않도록 한다. <br>
        <span>|</span> 바벨을 들어올리면서, 팔꿈치의 방향이 바깥을 향하지 않도록 한다.<br>
        <span>|</span> 아래 팔을 바닥과 수직으로 유지한다.<br>
      </div>
    </div>

  </div>
</div>
