  <div class="detail">


    <div class="how-to">
      <ol>
        <li>발을 골반 너비로 벌리고 선다.</li>
        <li>한 발을 넓은 보폭으로 내딛는다. </li>
        <li>앞 발을 90도로 굽혀주고 뒷 발을 무릎에 닫지 않을 정도로 앉는다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 앞 발의 발 뒤꿈치에 체중을 싣는다.<br>
        <span>|</span> 골반이 좌우로 치우치지 않도록 한다. <br>
        <span>|</span> 상체를 곧게 세우고 앞으로 기울지 않게 주의한다.<br>
      </div>
