    <div class="how-to">
      <ol>
        <li>기울어진 벤치에 누워 견갑대를 최대한 모으고 허리를 등방향으로 꺾어 벤치와 허리부분에 공간을 유지한다.</li>
        <li>덤벨을 들어 올린다.</li>
        <li>덤벨을 명치와 젖꼭지라인의 사이로 가져온다.</li>
        <li>대흉근을 이용해 덤벨을 밀어올린다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 견갑대를 최고점에서도 모은채로 유지한다.<br>
        <span>|</span> 턱을 들지 않는다.<br>
        <span>|</span> 너무 무리해서 팔을 내리지 않는다.<br>
