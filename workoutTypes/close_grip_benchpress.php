    <div class="how-to">
      <ol>
        <li>벤치에 누워 어깨 넓이 만큼 바벨을 잡는다.</li>
        <li>견갑대를 모으고 복근과 엉덩이에 힘을 준다.</li>
        <li>바벨을 들어 올린다.</li>
        <li>바벨을 내린다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 바벨을 너무 좁게 잡아 불안정한 상태로 운동을 하지 않는다.<br>
        <span>|</span> 몸 전체의 긴장을 유지한다.. <br>
        <span>|</span> 숨을 내쉬면서 바벨을 들어올리고, 들이쉬면서 바벨을 내린다. <br>
