  <div class="summary">
    <p>
      프론트 스쿼트는 바벨을 몸의 앞 쪽에 두고 하는 스쿼트로 하체와 함께 코어 근육을 발달시킬 수 있다. 다만 초보자가 하기에 어려움이 있으므로 주의가 필요하다.
    </p>
  </div>

  <div class="detail">


    <div class="how-to">
      <ol>
        <li>팔을 교차하여 손을 바 상단에 올리고 바벨을 어깨로 올린다.</li>
        <li>삼두근을 바닥과 평행하게 하여 어깨가 무게를 지탱하게 한다.</li>
        <li>등을 곧게 유지한 상태로 허벅지가 바닥과 평행일 될 때까지 골반을 내린다.</li>
        <li>골반을 올려 처음의 상태로 돌아온다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 팔꿈치를 높게 유지시켜 바벨이 어깨 위에 안전하게 놓이도록 한다.<br>
        <span>|</span> 등이 구부러지지 않도록 유지한다. <br>
        <span>|</span> 척추를 곧게 펴고, 가슴을 활짝 편 상태를 유지한다. <br>

      </div>
    </div>

