  <div class="summary">
    <p>
      벤치프레스는 벤치에 누운채로 가슴위의 바벨을 밀어올리는 동작으로 대흉근 발달에 커다란 효과가 있는 운동이다. '3대 운동'으로 불리우는 웨이트 트레이닝의 대표적인 운동이다.
    </p>
  </div>

  <div class="detail">


    <div class="how-to">
      <ol>
        <li>벤치에 누워 견갑대를 최대한 모으고 허리를 등방향으로 꺾어 벤치와 허리부분에 공간을 유지한다.</li>
        <li>바벨을 명치와 젖꼭지라인의 사이로 가져온다.</li>
        <li>대흉근을 이용해 바벨을 밀어올린다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 견갑대를 최고점에서도 모은채로 유지한다.<br>
        <span>|</span> 바벨을 잡을 때, 손꿈치에 바벨이 지나가도록 잡아, 손목에 무리가 가지 않도록 한다. <br>
        <span>|</span> 바벨을 들어올리면서, 팔꿈치의 방향이 바깥을 향하지 않도록 한다.<br>
        <span>|</span> 턱을 들지 않는다.<br>
      </div>
    </div>

  </div>
</div>
