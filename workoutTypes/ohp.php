<h3 class='title'>OHP(overhead press)</h3>
<div class="content-container">

  <div class="image-container">
    <img class='image'  alt="overhead-press" src="./workoutTypes/ohp.png">
  </div>

  <div class="summary">
    <p>
      오버헤드 프레스는 기본적인 어깨 전면부 운동으로 바벨을 삼각근 상부에 견착한 뒤 팔을 뻗어 머리위로 만세를 하는 동작이다.밀리터리 프레스라고도 불리운다.
    </p>
  </div>

  <div class="detail">


    <div class="how-to">
      <ol>
        <li>어깨 넓이로 발을 벌리고 삼각근상부에 바벨을 견착한다.</li>
        <li>숨을 들이쉬고 척추 중립을 유지한체 바벨을 위로 뻗는다.</li>
        <li>다시 천천히 삼각근상부에 바벨을 견착한다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 바벨을 들어올리면서. 팔꿈치의 방향이 바깥을을 향하지 않도록 한다.<br>
        <span>|</span> 바벨을 잡을 때, 손꿈치에 바벨이 지나가도록 잡아, 손목에 무리가 가지 않도록 한다. <br>
        <span>|</span> 머리를 움직이면서 바벨이 지면과 수직으로 움직일 수 있도록 한다<br>

      </div>
    </div>

  </div>
