  <div class="detail">


    <div class="how-to">
      <ol>
        <li>어깨보다 조금 넓은 간격으로 손등이 자신을 향하도록 바벨을 잡는다.</li>
        <li>다리를 어깨 넓이만큼 벌리고 무릎을 굽혀 하체를 고정한다.</li>
        <li>상체를 45도 각도로 숙인다. 이 때 등근육을 긴장시켜 허리를 아치형으로 만든다.</li>
        <li>바벨을 들어올린다.</li>
      </ol>
      <div class="warning">
        <span>|</span> 등근육의 긴장을 유지한다.<br>
        <span>|</span> 허리가 굽어지지 않도록 주의한다. <br>
        <span>|</span> 숨을 들이쉬면서 바벨을 들어올리고, 내쉬면서 바벨을 내린다. <br>
        <span>|</span> 시선은 항상 전방을 향하도록 한다. <br>
      </div>
